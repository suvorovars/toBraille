class TextToBraille:
    def __init__(self):
        self.txt_to_braille = {'а': ['*_', '__', '__'], 'б': ['*_', '*_', '__'], 'в': ['_*', '**', '_*'],
                               'г': ['**', '**', '__'], 'д': ['**', '_*', '__'], 'е': ['*_', '_*', '__'],
                               'ё': ['*_', '__', '_*'], 'ж': ['_*', '**', '__'], 'з': ['*_', '_*', '**'],
                               'и': ['_*', '*_', '__'], 'й': ['**', '*_', '**'], 'к': ['*_', '__', '*_'],
                               'л': ['*_', '*_', '*_'], 'м': ['**', '__', '*_'], 'н': ['**', '_*', '*_'],
                               'о': ['*_', '_*', '*_'], 'п': ['**', '*_', '*_'], 'р': ['*_', '**', '*_'],
                               'с': ['_*', '*_', '*_'], 'т': ['_*', '**', '*_'], 'у': ['*_', '__', '**'],
                               'ф': ['**', '*_', '__'], 'х': ['*_', '**', '__'], 'ц': ['**', '__', '__'],
                               'ч': ['**', '**', '*_'], 'ш': ['*_', '_*', '_*'], 'щ': ['**', '__', '**'],
                               'ъ': ['*_', '**', '**'], 'ы': ['_*', '*_', '**'], 'ь': ['_*', '**', '**'],
                               'э': ['_*', '*_', '_*'], 'ю': ['*_', '**', '_*'], 'я': ['**', '*_', '_*'],
                               '.': ['__', '**', '_*'], '!': ['__', '**', '*_'], '-': ['__', '__', '**'],
                               '«': ['__', '*_', '**'], '»': ['__', '_*', '**'], '(': ['*_', '*_', '_*'],
                               ')': ['_*', '_*', '*_'], ',': ['__', '*_', '__'], '?': ['__', '*_', '_*'],
                               ' ': ['  ', '  ', '  ']}

    def to_braille(self, txt):
        txt = txt.lower()
        x = ''
        y = ''
        z = ''
        for i in txt:
            x += str(self.txt_to_braille[i][0]) + ' '
            y += str(self.txt_to_braille[i][1]) + ' '
            z += str(self.txt_to_braille[i][2]) + ' '
        return x + '\n' + y + '\n' + z


a = TextToBraille()

print(a.to_braille("привет мир!!!"))